  /// حساب مسافة ليفنشتاين (أقل عدد عمليات تحويل من a إلى b)
  static int levenshteinDistance(String a, String b) {
    if (a == b) return 0;
    if (a.isEmpty) return b.length;
    if (b.isEmpty) return a.length;

    // تحسين الذاكرة: صف واحد فقط
    List<int> prev = List<int>.generate(b.length + 1, (j) => j);
    List<int> curr = List<int>.filled(b.length + 1, 0);

    for (int i = 1; i <= a.length; i++) {
      curr[0] = i;
      for (int j = 1; j <= b.length; j++) {
        final cost = a[i - 1] == b[j - 1] ? 0 : 1;
        curr[j] = [
          prev[j] + 1,       // حذف
          curr[j - 1] + 1,   // إضافة
          prev[j - 1] + cost // استبدال
        ].reduce((x, y) => x < y ? x : y);
      }
      final tmp = prev;
      prev = curr;
      curr = tmp;
    }
    return prev[b.length];
  }

  /// مطابقة ضبابية تعيد نسبة تشابه بين 0.0 و 1.0
  static double fuzzyMatch(String a, String b) {
    if (a.isEmpty && b.isEmpty) return 1.0;
    if (a.isEmpty || b.isEmpty) return 0.0;
    if (a == b) return 1.0;

    final maxLen = a.length > b.length ? a.length : b.length;
    final dist = levenshteinDistance(a, b);
    return 1.0 - (dist / maxLen);
  }

  /// مطابقة ضبابية مع عتبة — تعيد الكلمة المطابقة أو null
  static String? fuzzyFind(String input, List<String> candidates, {double threshold = 0.75}) {
    String? bestMatch;
    double bestScore = 0;

    for (final candidate in candidates) {
      final score = fuzzyMatch(input, candidate);
      if (score >= threshold && score > bestScore) {
        bestScore = score;
        bestMatch = candidate;
      }
    }
    return bestMatch;
  }

  /// مطابقة ضبابية جزئية — تبحث في كل كلمة داخل النص
  static List<String> fuzzyFindAll(String text, List<String> candidates, {double threshold = 0.78}) {
    final words = text.split(' ').where((w) => w.length > 2).toList();
    final results = <String>[];

    for (final word in words) {
      for (final candidate in candidates) {
        final score = fuzzyMatch(word, candidate);
        if (score >= threshold) {
          results.add(candidate);
        }
      }
    }
    return results;
  }

