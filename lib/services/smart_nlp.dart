    if (RegExp(r'اشراف داعم|supportive supervision|اشرافي داعم|زياره اشرافيه داعمه|الاشراف الداعم')
        .hasMatch(n)) return 'supportive_supervision';

    // ── نظام المعلومات الصحية HMIS ──
    if (RegExp(r'HMIS|hmis|نظام المعلومات الصحيه|نظام معلومات صحيه|health management information')
        .hasMatch(n)) return 'hmis_reporting';

    // ── DHIS2 ──
    if (RegExp(r'DHIS2|dhis2|ديس تو|دحيس|نظام معلومات الصحه|District Health Information')
        .hasMatch(n)) return 'hmis_reporting';

    // ── التخطيط الدقيق ──
    if (RegExp(r'تخطيط دقيق|ميكروبلان|microplanning|مايكروبلانينج|تخطيط محلي|خطة تشغيليه|خطه تنفيذيه')
        .hasMatch(n)) return 'microplanning';

    // ── الاستجابة للأوبئة ──
    if (RegExp(r'استجابه|استجابه للاوبئه|استجابه وبائيه|outbreak response|مكافحة الاوبئة|وبائيات|فاشيه')
