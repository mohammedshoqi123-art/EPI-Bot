  /// تحليل المشاعر في نص المستخدم
  static String detectEmotion(String text) {
    final n = normalize(text);

    // خوف
    if (RegExp(r'خايف|خايفه|يخوف|خوف|مرعوب|فزع|رعب|قلقان|قلقانه|متوتر|قلق|خايف عليه')
        .hasMatch(n)) return 'خوف';

    // غضب
    if (RegExp(r'زعلان|زعلانه|غضبان|معصب|مقهور|مستاء|غاضب|غاضبه|زعل|معترض')
        .hasMatch(n)) return 'غضب';

    // حيرة
    if (RegExp(r'محتار|محتاره|ما ادري|ما اعرف|مشوش|تائه|حيران|حيرانه|محير')
        .hasMatch(n)) return 'حيرة';

    // ارتياح
    if (RegExp(r'مرتاح|مرتاحه|مبسوط|مبسوطه|فرحان|فرحانه|راضي|راضيه|حمدلله|الحمد لله')
        .hasMatch(n)) return 'ارتياح';

    // ألم
    if (RegExp(r'يالم|يتالم|وجع|يعور|الم|تعبان|تعبانه|مريض')
        .hasMatch(n)) return 'ألم';

    // رفض
    if (hasNegation(n)) return 'رفض';

    return 'محايد';
  }

  /// هل المستخدم يحتاج دعم نفسي؟
  static bool needsEmotionalSupport(String text) {
    final emotion = detectEmotion(text);
    return emotion == 'خوف' || emotion == 'غضب' || emotion == 'حيرة' || emotion == 'ألم';
