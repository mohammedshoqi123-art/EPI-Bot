  /// تحليل ما إذا كان النص يشير إلى تغيير موضوع
  static bool isTopicChange(String current, String? previous) {
    if (previous == null) return false;

    final currentIntent = detectIntent(normalize(current));
    final previousIntent = detectIntent(normalize(previous));

    if (currentIntent == 'greeting' || currentIntent == 'thanking') return false;
    if (currentIntent != previousIntent && currentIntent != 'follow_up' && currentIntent != 'general') {
      return true;
    }
    return false;
  }

  /// استخراج السياق الطبي من المحادثة
  static Map<String, dynamic> extractMedicalContext(String text) {
    final n = normalize(text);
    final context = <String, dynamic>{};

    // العمر
    final age = extractAge(text);
    if (age != null) {
      context['age_weeks'] = age.weeks;
      context['age_months'] = age.months;
      context['age_days'] = age.days;
    }

    // اللقاح المذكور
    final vaccine = detectVaccineMention(n);
    if (vaccine != null) context['vaccine'] = vaccine;

    // المرض المذكور
    final disease = detectDiseaseMention(n);
    if (disease != null) context['disease'] = disease;

    // درجة الحرارة
    final temp = extractTemperature(text);
    if (temp != null) context['temperature'] = temp;

    // الشدة
    context['severity'] = detectSeverity(n);

    // النية
    context['intent'] = detectIntent(n);

    // النفي
    context['negation'] = hasNegation(text);

    // المشاعر
    context['emotion'] = detectEmotion(text);

    return context;
  }

