  /// استخراج الكيانات الطبية من النص
  static Map<String, List<String>> extractEntities(String text) {
    final n = normalize(text);
    final entities = <String, List<String>>{
      'vaccines': [],
      'diseases': [],
      'symptoms': [],
      'ages': [],
    };

    // لقاحات
    final vaccine = detectVaccineMention(n);
    if (vaccine != null) entities['vaccines']!.add(vaccine);

    // أمراض
    final disease = detectDiseaseMention(n);
    if (disease != null) entities['diseases']!.add(disease);

    // أعراض
    final symptomKeywords = ['حراره', 'الم', 'تورم', 'احمرار', 'تشنج', 'بكاء', 'سخونه', 'انتفاخ', 'حمى'];
    for (final sk in symptomKeywords) {
      if (n.contains(sk)) entities['symptoms']!.add(sk);
    }

    // أعمار
    final age = extractAge(text);
    if (age != null) {
      entities['ages']!.add('${age.months}ش ${age.weeks}أ ${age.days}ي');
    }

    return entities;
  }

  /// تجميع نية كاملة مع تصحيح الأخطاء
  static IntentResult fullAnalysis(String text, {String? previousIntent, String? lastTopic}) {
    // 1. تصحيح الأخطاء الإملائية
    final corrected = correctTypos(text);
    // 2. تطبيع
    final normalized = normalize(corrected);
    // 3. اكتشاف النية مع الثقة
    final result = detectIntentWithConfidence(normalized, previousIntent: previousIntent, lastTopic: lastTopic);
    // 4. استخراج السياق الطبي
    final medicalContext = extractMedicalContext(corrected);
    // 5. تحليل المشاعر
    final emotion = detectEmotion(normalized);

    return IntentResult(
      result.intent,
      result.confidence,
      allScores: {
        ...result.allScores,
        'emotion': emotion,
        if (medicalContext.containsKey('vaccine')) 'vaccine': medicalContext['vaccine'].toString(),
        if (medicalContext.containsKey('disease')) 'disease': medicalContext['disease'].toString(),
        if (medicalContext.containsKey('temperature')) 'temperature': medicalContext['temperature'].toString(),
        if (medicalContext.containsKey('severity')) 'severity': medicalContext['severity'].toString(),
      },
    );
  }

