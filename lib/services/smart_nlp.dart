      confidence = 0.30 + ((maxScore - 1.0) / 10.0).clamp(0.0, 0.25);
    } else {
      confidence = 0.15 + (maxScore / 10.0).clamp(0.0, 0.15);
    }

    // خفض الثقة إذا كان هناك تنافس شديد بين نيتين
    if (sorted.length >= 2) {
      final secondScore = sorted[1].value;
      final gap = maxScore - secondScore;
      if (gap < 0.5) {
        confidence *= 0.8; // خفض 20% بسبب التنافس
      } else if (gap < 1.0) {
        confidence *= 0.9; // خفض 10%
      }
    }

    confidence = confidence.clamp(0.05, 0.99);

    // تحويل allScores إلى نسب
    final normalizedScores = <String, double>{};
    final totalPoints = scores.values.fold(0.0, (a, b) => a + b);
    for (final e in scores.entries) {
      normalizedScores[e.key] = totalPoints > 0 ? e.value / totalPoints : 0;
    }

    return IntentResult(best.key, confidence, allScores: normalizedScores);
  }

