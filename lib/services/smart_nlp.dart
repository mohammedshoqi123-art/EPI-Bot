        .hasMatch(n)) return 'school_immunization';

    // ── إدارة سلسلة التبريد ──
    if (RegExp(r'سلسله بارده|سلسلة التبريد|اداره سلسله التبريد|cold chain management|تبريد اللقاحات|حفظ اللقاح')
        .hasMatch(n)) return 'cold_chain_management';

    // ── التخلص من النفايات ──
    if (RegExp(r'نفايات|التخلص من النفايات|waste management|نفايات طبيه|نفايات حاده|تخلص من ابر|تخلص من محاقن|سلامه الحقن')
        .hasMatch(n)) return 'waste_management';

    // ── تخطيط الجلسات ──
    if (RegExp(r'تخطيط الجلسات|session planning|جلسه تطعيم|جلسات تطعيم|تنظيم الجلسه|تخطيط جلسه|اعداد الجلسه')
        .hasMatch(n)) return 'session_planning';

    // ── تعزيز الطلب ──
    if (RegExp(r'تعزيز الطلب|demand generation|توعيه مجتمعيه|تسويق اجتماعي|تشجيع التحصين|تعزيز الطلب المجتمعي')
        .hasMatch(n)) return 'demand_generation';

    // ── المشاركة المجتمعية ──
    if (RegExp(r'مشاركه مجتمعيه|community engagement|مشاركة المجتمع|انخراط مجتمعي|تعبئه مجتمعيه|قاده مجتمعيين')
        .hasMatch(n)) return 'community_engagement';

    // ── جودة البيانات ──
    if (RegExp(r'جوده البيانات|data quality|دقه البيانات|موثوقيه البيانات|سلامه البيانات|تحقق البيانات|تحقق من البيانات')
        .hasMatch(n)) return 'data_quality';

    // ── إدارة المخزون ──
    if (RegExp(r'اداره المخزون|stock management|حصر اللقاحات|جرد|مخزون لقاحات|رصيد|نواقص لقاحات|طلب لقاحات')
        .hasMatch(n)) return 'stock_management';

    // ── تحليل التسرب ──
    if (RegExp(r'تحليل التسرب|drop out analysis|dropout|تسرب التحصين|متسربين|نسبه التسرب|فجوه التسرب')
        .hasMatch(n)) return 'drop_out_analysis';

    // ── تتبع المتخلفين ──
    if (RegExp(r'تتبع المتخلفين|defaulter tracing|متخلفين|متاخرين|تتبع الحالات المتخلفه|استرجاع المتخلفين|بحث عن المتخلفين')
        .hasMatch(n)) return 'defaulter_tracing';

    // ── سياسة القارورة المفتوحة ──
    if (RegExp(r'سياسه القاروره المفتوحه|open vial policy|OVP|قاروره مفتوحه|استخدام القاروره|قارورة مفتوحة|سياسة القارورة')
        .hasMatch(n)) return 'open_vial_policy';

    // ═══════ النيات الموجودة سابقاً ═══════

    // ── أشراف عام ──
    if (RegExp(r'اشراف|supervision|زياره اشرافيه|supportive|تقييم اداء|checklist|قائمه مراجعه|زياره ميدانيه')
        .hasMatch(n)) return 'supervision';

    // ── إدارة عامة ──
    if (RegExp(r'مدير مكتب|مدير محافظه|تخطيط|اداره صحيه|مؤشرات اداء|KPI|تقارير|اداره المكتب')
        .hasMatch(n)) return 'management';

    // ── تغذية ──
    if (RegExp(r'تغذيه|اكل|غذاء|ياكل|رضاعه|يرضع|حليب|فيتامين|vitamin|ياكل كويس')
        .hasMatch(n)) return 'nutrition';

    // ── سلسلة تبريد (عامة) ──
    if (RegExp(r'تبريد|سلسله بارده|ثلاجه|تخزين|vvm|درجه حراره|تخزين اللقاح')
        .hasMatch(n)) return 'cold_chain';

    // ── متابعة ──
    if (RegExp(r'^(نعم|ايوه|ايه|اي|يب|ايه نعم|ايه اي|لا|كم|ليه|ليش|اشرح|وضح|بالتفصيل|تفاصيل|طيب|تمام|واضح|فهمت|اوكي|زين|اوك|ياب|اوك شكرا|كذا شكرا)')
        .hasMatch(n)) return 'follow_up';

    // ── سفر ──
    if (RegExp(r'سفر|مسافر|سياحه|مطار|سافر').hasMatch(n)) return 'travel';

    // ── تاريخ ──
    if (RegExp(r'تاريخ|متى بدأ|متى بدا|متى انشئ|متى ابتدي').hasMatch(n)) return 'history';

    // ── فوائد ──
    if (RegExp(r'فوائد|ليش نطعم|ليش مهم|فايده|ليش لازم|وش الفايده|ليه نطعم|ليه نطعمه').hasMatch(n)) return 'benefits';

    // ── تذكير ──
    if (RegExp(r'تذكير|ذكرني|ذكرني ب|موعد|reminder|تنبيه|نبهني').hasMatch(n)) return 'reminder';

    // ── تقييم / رأي ──
    if (RegExp(r'تقييم|رأيك|وش رايك|ايش رايك|good|ممتاز|سيء|كويس|حلو|زفت').hasMatch(n)) return 'feedback';

    // ── ليش ──
    if (n.startsWith('ليش') || n.startsWith('لماذا') || n.startsWith('ليه') || n.startsWith('ليهذا')) {
      if (previousIntent == 'myths' || previousIntent == 'benefits') return previousIntent!;
      if (n.contains('تطعيم') || n.contains('لقاح')) return 'benefits';
      return 'benefits';
    }

    // ── أمراض ──
    if (RegExp(r'مرض|امراض|عدوى|وباء').hasMatch(n)) return 'diseases';

    // ── مقارنة ──
    if (hasComparison(n)) return 'vaccine_types';

    // ── تشخيص حالة الطفل ──
    if (RegExp(r'مريض|تعبان|مريضه|ما ياكل|يرفض|ما يبي|مريض|مريضه|ما يرضع|مريض شوي|مريضه شوي')
        .hasMatch(n)) return 'child_sick';

    // ── رصد وبائي ──
    if (RegExp(r'رصد|ترصد|نظام رصد|رصد الامراض|مراقبه وبائيه').hasMatch(n)) return 'surveillance';

    // ── تدريب وبناء قدرات ──
    if (RegExp(r'تدريب|بناء قدرات|ورشه عمل|ورشه تدريبيه|محاضره|تعليم مستمر').hasMatch(n)) return 'training';

    // ── تنسيق وشراكة ──
    if (RegExp(r'تنسيق|شراكه|تعاون|تكامل|تنسيق بين القطاعات').hasMatch(n)) return 'coordination';

    // ── سلامة الحقن ──
    if (RegExp(r'سلامه الحقن|injection safety|امان الحقن|محاقن آمنه|ابر امنه').hasMatch(n)) return 'injection_safety';

    // ── تغذية راجعة ──
    if (RegExp(r'تغذيه راجعه|feedback|ملاحظات|ملاحظه|اقتراح').hasMatch(n)) return 'feedback';

    // ── رعاية أمومة ──
    if (RegExp(r'رعايه امومه|امومه|حمل|ولاده|ما قبل الولاده|ما بعد الولاده|postnatal|antenatal').hasMatch(n)) return 'maternal_care';

    // ── سياق محادثة: إذا كان السؤال قصير وغامض ──
    if (previousIntent != null && previousIntent != 'general' && previousIntent != 'greeting' && previousIntent != 'thanking') {
      if (n.length < 20 && !_isNewTopic(n)) return previousIntent;
    }

    return 'general';
  }

  static bool _isNewTopic(String n) {
    return RegExp(r'طيب|بس|تمام|غير|اوكي|زين|اوك|بس كذا|تمام شكرا').hasMatch(n);
  }

  // ══════════════════════════════════════════════════════════════════
